package org.elasticsearch.search.aggregations.bucket.histogram;

/**
 * An aggregator that assigns documents to buckets by a range valued field.
 *
 * @param <N> The type of range and bucket bounds.
 */
public interface RangeFieldBucketAggregator<N extends Number> {

    /**
     * Returns information about the currently processed range and bucket.
     *
     * Can be called by subaggregations while their collect methods are invoked.
     */
    RangeFieldBucketAggregatorCollectContext<N> getCollectContext();

}
